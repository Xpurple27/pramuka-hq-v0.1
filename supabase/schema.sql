create policy "owners manage attendance" on public.attendance for all to authenticated
using (exists(select 1 from public.training_sessions session where session.id = training_session_id and public.owns_gudep(session.gudep_id)))
with check (
  exists(select 1 from public.training_sessions session where session.id = training_session_id and public.owns_gudep(session.gudep_id))
  and exists(select 1 from public.members member where member.id = member_id and public.owns_gudep(member.gudep_id))
);

