  validated_at timestamptz,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  unique(member_id, skk_item_id, level)
);

create or replace function public.validate_scouthub_relations()
returns trigger language plpgsql set search_path = public as $$
declare
  related_gudep uuid;
  session_gudep uuid;
  member_gudep uuid;
begin
  if tg_table_name = 'members' and new.patrol_id is not null then
    select gudep_id into related_gudep from public.patrols where id = new.patrol_id;
    if related_gudep is distinct from new.gudep_id then raise exception 'Regu dan anggota harus berasal dari gudep yang sama'; end if;
  elsif tg_table_name = 'patrols' then
    if new.leader_member_id is not null then
      select gudep_id into related_gudep from public.members where id = new.leader_member_id;
      if related_gudep is distinct from new.gudep_id then raise exception 'Pemimpin regu harus berasal dari gudep yang sama'; end if;
    end if;
    if new.vice_leader_member_id is not null then
      select gudep_id into related_gudep from public.members where id = new.vice_leader_member_id;
      if related_gudep is distinct from new.gudep_id then raise exception 'Wakil pemimpin harus berasal dari gudep yang sama'; end if;
    end if;
  elsif tg_table_name = 'attendance' then
    select gudep_id into session_gudep from public.training_sessions where id = new.training_session_id;
    select gudep_id into member_gudep from public.members where id = new.member_id;
    if session_gudep is distinct from member_gudep then raise exception 'Pertemuan dan anggota harus berasal dari gudep yang sama'; end if;
  end if;
  return new;
end;
$$;

