create or replace function public.set_updated_at()
returns trigger language plpgsql as $$
begin
  new.updated_at = timezone('utc', now());
  return new;
end;
$$;

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  full_name text not null default '',
  email text,
  phone text,
  role text not null default 'pembina' check (role in ('pembina', 'admin')),
  avatar_url text,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.gudep (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  school_name text not null,
  gudep_number text,
  address text,
  level text not null default 'Penggalang',
  academic_year text not null default '2026/2027',
  is_active boolean not null default true,
  created_by uuid not null references auth.users(id) on delete cascade,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.members (
  id uuid primary key default gen_random_uuid(),
  gudep_id uuid not null references public.gudep(id) on delete cascade,
  name text not null,
  student_number text,
  nisn text,
  gender text not null check (gender in ('L', 'P')),
  class_name text not null,
  scout_level text not null default 'Penggalang Ramu',
  parent_phone text,
  status text not null default 'Aktif' check (status in ('Aktif', 'Cuti', 'Alumni')),
  notes text,
  joined_at date not null default current_date,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.patrols (
  id uuid primary key default gen_random_uuid(),
  gudep_id uuid not null references public.gudep(id) on delete cascade,
  name text not null,
  patrol_type text not null default 'Putra' check (patrol_type in ('Putra', 'Putri')),
  leader_member_id uuid references public.members(id) on delete set null,
  vice_leader_member_id uuid references public.members(id) on delete set null,
  color text not null default '#10b981',
  is_active boolean not null default true,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  unique(gudep_id, name)
);

